type SupportedLanguage = 'en' | 'fr';
const SUPPORTED_LANGUAGES: SupportedLanguage[] = ['en', 'fr'];
const DEFAULT_LANGUAGE: SupportedLanguage = 'fr';
const STORAGE_KEY = 'preferred-language';
const I18N_EVENT_MISSING = 'i18n:missing-key';
const I18N_EVENT_FALLBACK = 'i18n:fallback';
const I18N_EVENT_FAILED = 'i18n:failed-loading';
const canDispatchI18nEvent = typeof window !== 'undefined' && typeof window.dispatchEvent === 'function' && typeof CustomEvent === 'function';
const emitI18nEvent = (eventName: string, detail: Record<string, unknown>) => {
  if (!canDispatchI18nEvent) return;
  window.dispatchEvent(new CustomEvent(eventName, { detail: { ...detail, timestamp: Date.now() } }));
};

const isSupportedLanguage = (lang: string | null): lang is SupportedLanguage =>
  SUPPORTED_LANGUAGES.includes(lang as SupportedLanguage);

const getQueryLanguage = (): SupportedLanguage | null => {
  if (typeof window === 'undefined') return null;
  const queryLang = new URLSearchParams(window.location.search).get('lang');
  if (!isSupportedLanguage(queryLang)) return null;
  localStorage.setItem(STORAGE_KEY, queryLang);
  return queryLang;
};

const getSavedLanguage = (): SupportedLanguage | null => {
  if (typeof window === 'undefined') return null;
  const saved = localStorage.getItem(STORAGE_KEY);
  return isSupportedLanguage(saved) ? saved : null;
};

const getBrowserLanguage = (): SupportedLanguage | null => {
  if (typeof navigator === 'undefined') return null;
  const browserLang = navigator.language?.slice(0, 2) ?? null;
  return isSupportedLanguage(browserLang) ? browserLang : null;
};

const getInitialLanguage = (): SupportedLanguage =>
  getQueryLanguage() ?? getSavedLanguage() ?? getBrowserLanguage() ?? DEFAULT_LANGUAGE;

const initialLang = getInitialLanguage();
if (typeof document !== 'undefined') {
  document.documentElement.lang = initialLang;
}

